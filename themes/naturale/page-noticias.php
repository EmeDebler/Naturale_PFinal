       ¡Achú!: Recetas fáciles y naturales para combatir la alergia primaveral</h5>
       <p class="card-text parrafo">A veces los síntomas de la alergia primaveral no dan tregua, sin embargo, existen algunos remedios naturales que pueden ayudar a disminuirlos. Una experta de la Universidad del Pacífico los da a conocer.

       El remedio más común para combatir la alergia son los tradicionales antihistamínicos químicos, pero hay personas alérgicas que evitan consumirlos, ya que algunos dan sueño o porque simplemente prefieren evitar medicarse.

       Si bien todo cuadro alérgico debe ser adecuadamente diagnosticado por un médico especialista, existen algunos remedios caseros o naturales que pueden ayudar a paliar los molestos síntomas de la alergia estacional.

       Carmen Gloria Pizarro, académica de la carrera de Agronomía de la Universidad del Pacífico, menciona los siguientes:

       1. Manzanilla de Castilla o manzanilla común: poseen propiedades antihistamínicas, antiespasmódicas  y antiinflamatorias. Se debe usar la flor de esta planta en la preparación de infusiones.

       2. Aloe vera: conocido también en otras latitudes como sávila o sábila, regenera las células de la piel, oxigena la sangre, limpia el tracto digestivo, lo mantiene libre de bacterias, y depura y tonifica el organismo, dado que potencia el sistema inmunológico con los polisacáridos que contiene. Se usa frecuentemente como extracto fluido y como crema. El primer método consiste en limpiar un trozo de esta hoja suculenta, a la que se le eliminan bordes y se pela la corteza; luego esta pulpa se puede mezclar con yogurt o con jugo de frutas o consumir directamente. Y el segundo método consiste en cortar un trozo de dos dedos, eliminar bordes y los extremos, y aplicarla directamente sobre la piel.

       3. Avena sativa: se considera dentro de aquellas especies que pueden ser utilizadas para tratar alergias en forma de jabón y geles de baño, ya que la avena contiene minerales como el cobre, cobalto, hierro, magnesio y zinc, junto con vitaminas tan importantes como la A, B1 y B2. Debido a la riqueza de estos nutrientes, su primera función es nutritiva, pero también se conoce como un excelente desinflamatorio, por lo que se convierte en un buen auxiliar de cuadros alérgicos.

       4. Cedro del Líbano: o Cedrus libani, puede ser usado como emplastos y bálsamos, por sus propiedades antisépticas y para el combate de alergias y salpullidos.

       La especialista de la Universidad del Pacífico también agrega a la lista de consejos incorporar aquellos alimentos con flavonoides, en especial la quercetina. “Esta es una sustancia a la que se le adjudica un poder antialérgico, la cual se encuentra en abundancia en ajos, cebollas, manzanas, pera, avena, cerezas, espinacas, bruselas y mangos, entre otros”, apunta.

       De acuerdo a estas recomendaciones, una dieta anti-alergias o que ayuda a aliviar los molestos síntomas de las alergias, debe ser rica en frutas y verduras e incorporar un consumo periódico de legumbres, incluyendo diariamente tres porciones de verduras y dos de frutas, según el listado.
